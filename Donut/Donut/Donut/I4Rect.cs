using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Charlotte.Donut
{
	public class I4Rect
	{
		public int L;
		public int T;
		public int W;
		public int H;

		public I4Rect()
		{ }

		public I4Rect(int l, int t, int w, int h)
		{
			this.L = l;
			this.T = t;
			this.W = w;
			this.H = h;
		}
	}
}
